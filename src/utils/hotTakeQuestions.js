// Get random questions for a game
export function getRandomQuestions(count = 5) {
  // Get questions from different categories for variety
  const categories = [...new Set(HOT_TAKE_QUESTIONS.map(q => q.category))]
  const selected = []
  const used = new Set()
  
  // Try to get one from each category first
  for (const category of categories.sort(() => Math.random() - 0.5)) {
    if (selected.length >= count) break
    
    const categoryQuestions = HOT_TAKE_QUESTIONS.filter(
      (q, i) => q.category === category && !used.has(i)
    )
    
    if (categoryQuestions.length > 0) {
      const randomIndex = HOT_TAKE_QUESTIONS.indexOf(
        categoryQuestions[Math.floor(Math.random() * categoryQuestions.length)]
      )
      selected.push(HOT_TAKE_QUESTIONS[randomIndex])
      used.add(randomIndex)
    }
  }
  
  // Fill remaining with random questions
  while (selected.length < count) {
    const remaining = HOT_TAKE_QUESTIONS.filter((_, i) => !used.has(i))
    if (remaining.length === 0) break
    
    const q = remaining[Math.floor(Math.random() * remaining.length)]
    const index = HOT_TAKE_QUESTIONS.indexOf(q)
    selected.push(q)
    used.add(index)
  }
  
  return selected
}

// Get question by index
export function getQuestion(questions, index) {
  return questions[index] || null
}
