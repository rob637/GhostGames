// Get random prompts with category variety
export function getRandomMostLikelyPrompts(count = 10) {
  const categories = [...new Set(MOST_LIKELY_PROMPTS.map(p => p.category))]
  const selected = []
  const usedCategories = new Set()
  
  for (let i = 0; i < count; i++) {
    // Get prompts preferring unused categories
    let pool = MOST_LIKELY_PROMPTS.filter(p => 
      !selected.includes(p) &&
      !usedCategories.has(p.category)
    )
    
    // Fallback: allow reused categories if needed
    if (pool.length === 0) {
      pool = MOST_LIKELY_PROMPTS.filter(p => !selected.includes(p))
    }
    
    if (pool.length > 0) {
      const prompt = pool[Math.floor(Math.random() * pool.length)]
      selected.push(prompt)
      usedCategories.add(prompt.category)
    }
  }
  
  return selected.map((p, i) => ({
    ...p,
    id: i,
  }))
}
