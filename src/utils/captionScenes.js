// Get random scenes with category variety  
export function getRandomScenes(count = 6) {
  const categories = [...new Set(CAPTION_SCENES.map(s => s.category))]
  const selected = []
  const usedCategories = new Set()
  
  for (let i = 0; i < count; i++) {
    // Prefer unused categories
    let pool = CAPTION_SCENES.filter(s => 
      !selected.includes(s) && !usedCategories.has(s.category)
    )
    
    // Fallback: allow reused categories
    if (pool.length === 0) {
      pool = CAPTION_SCENES.filter(s => !selected.includes(s))
    }
    
    if (pool.length > 0) {
      const scene = pool[Math.floor(Math.random() * pool.length)]
      selected.push(scene)
      usedCategories.add(scene.category)
    }
  }
  
  return selected
}

// Get scene by index
export function getScene(scenes, index) {
  return scenes[index] || null
}
