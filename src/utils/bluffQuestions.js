// Get random questions with category variety
export function getRandomBluffQuestions(count = 8) {
  const categories = [...new Set(BLUFF_BATTLE_QUESTIONS.map(q => q.category))]
  const selected = []
  const usedCategories = new Set()
  
  for (let i = 0; i < count; i++) {
    // Get questions preferring unused categories
    let pool = BLUFF_BATTLE_QUESTIONS.filter(q => 
      !selected.includes(q) &&
      !usedCategories.has(q.category)
    )
    
    // Fallback: allow reused categories if needed
    if (pool.length === 0) {
      pool = BLUFF_BATTLE_QUESTIONS.filter(q => !selected.includes(q))
    }
    
    if (pool.length > 0) {
      const question = pool[Math.floor(Math.random() * pool.length)]
      selected.push(question)
      usedCategories.add(question.category)
    }
  }
  
  return selected.map((q, i) => ({
    ...q,
    id: i,
  }))
}
